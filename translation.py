@app.route('/', methods=['GET', 'POST'])
def translate():
    translated_text = ''
    detected_language = ''
    full_detected_language = ''
    target_language = ''
    
    if request.method == 'POST':
        text = request.form['text']
        target_language = request.form['target_language']
        
        # Language detection and conversion to full name
        detected_language = detect(text)
        full_detected_language = languages.get(detected_language, detected_language)
        
        api_key = 'AIzaSyDFKpqVYNdSdI8CL2Ror1XrQcgahV5LCnE'  
        url = 'https://translation.googleapis.com/language/translate/v2'
        params = {
            'q': text,
            'source': detected_language,
            'target': target_language,
            'format': 'text',
            'key': api_key
        }
        response = requests.post(url, params=params)
        if response.status_code == 200:
            result = response.json()
            translated_text = result['data']['translations'][0]['translatedText']
        else:
            translated_text = 'Error during translation. Please check your API key and try again.'

    return render_template('index.html', translated_text=translated_text, full_detected_language=full_detected_language, target_language=target_language, languages=languages)

